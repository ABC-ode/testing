<h4>Which universities can I apply to for a student?</h4>
<p>A separate territorial sheet exists for each institute / agent. This is provided to you with form access. Refer to this sheet to see which universities can you apply for a student.</p>

<h4>I submitted an application but did not receive a confirmation email. Do I need to apply again?</h4>
<p>In some cases, an email does not arrive due to a server failure. You are advised to check your spam / junk folder. If still no luck, click the contact button on the top right of this page to speak to the team.</p>

<h4>I am missing some key documents for a student. Can I still apply and submit these documents later?</h4>
<p>You can apply however this will delay the process and we are unlikely to get an update until we have reviewed complete documents for the student.</p>

<h4>I’m unable to navigate through the form, Can I get assistance?</h4>
<p>Our team is always ready to help. Simply click contact in the top right corner of your screen and you will be taken to a chat where you can explain the issue, upload issue screenshots and more.</p>

<h4>How do I know how much commission have I earned to date?</h4>
<p>You can contact your representative who will provide you with this information. You can also ask them for an official commission status update emailed from our partnerships team.</p>

